def area(largura, comprimento):
    print(f'A largura é :{largura}')
    print(f'O comprimento é :{comprimeto}')
    ar = largura * comprimento
    print(f'A área de um terreno {largura} X {comprimento} é de {ar} metros quadrados')
    
print('     Controle de Terrenos')
print('-'*30)
largura = float(input('Digite a largula (m): '))
comprimeto = float(input('Digite o comprimento (m): '))

